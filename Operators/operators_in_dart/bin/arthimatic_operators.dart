// Arthematic Operators are

// (+) Addition

num sum(num1, num2) {
  return num1 + num2;
}

// (-) Substraction

num substract(num1, num2) {
  return num1 - num2;
}

// (*) Multiplication

num multiply(num1, num2) {
  return num1 * num2;
}

// (/) Division return type maybe int or double

num divide(num1, num2) {
  return num1 / num2;
}

// (~/) Divide return an integer

num divideExclusive(num1, num2) {
  return num1 ~/ num2;
}

// Modulus Operator (Remainder)

num modoulus(num1, num2) {
  return num1 % num2;
}
